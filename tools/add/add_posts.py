def add_posts():
    with app.app_context():
        # 计数
        count = 0
        for p_data in new_posts:
            # 检查标题是否已存在，防止重复添加
            exists = Post.query.filter_by(title=p_data['title']).first()
            if not exists:
                post = Post(
                    title=p_data['title'],
                    category=p_data['category'],
                    content=p_data['content'],
                    summary=p_data['summary'],
                    tags=p_data['tags'],
                    created_at=datetime.now()
                )
                db.session.add(post)
                count += 1
                print(f"[新增] {p_data['title']}")
            else:
                print(f"[跳过] {p_data['title']} (已存在)")
        
        db.session.commit()
        print("-" * 30)
        print(f"成功注入 {count} 篇文章到个人宇宙数据库！")

if __name__ == "__main__":
    add_posts()