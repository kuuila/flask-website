def add_fictions():
    with app.app_context():
        count = 0
        for p_data in new_fictions:
            # 查重
            exists = Post.query.filter_by(title=p_data['title']).first()
            if not exists:
                post = Post(
                    title=p_data['title'],
                    category=p_data['category'],
                    content=p_data['content'],
                    summary=p_data['summary'],
                    tags=p_data['tags'],
                    created_at=datetime.now()
                )
                db.session.add(post)
                count += 1
                print(f"[微小说] {p_data['title']} -> 入库")
            else:
                print(f"[跳过] {p_data['title']}")
        
        db.session.commit()
        print("-" * 30)
        print(f"成功注入 {count} 篇极具冲突感的微小说！")

if __name__ == "__main__":
    add_fictions()