def add_kcy_posts():
    with app.app_context():
        count = 0
        for p_data in new_kcy_posts:
            # 查重
            exists = Post.query.filter_by(title=p_data['title']).first()
            if not exists:
                post = Post(
                    title=p_data['title'],
                    category=p_data['category'],
                    content=p_data['content'],
                    summary=p_data['summary'],
                    tags=p_data['tags'],
                    created_at=datetime.now()
                )
                db.session.add(post)
                count += 1
                print(f"[Kcymaerxthaere] {p_data['title']} -> 融合完毕")
            else:
                print(f"[跳过] {p_data['title']}")
        
        db.session.commit()
        print("-" * 30)
        print(f"成功注入 {count} 篇平行宇宙铭文概念文章！")

if __name__ == "__main__":
    add_kcy_posts()
